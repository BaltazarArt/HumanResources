    private void txt_ageKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_ageKeyTyped
        char solonumero = evt.getKeyChar();
        if (txt_age.getText().length() >= 2) {
            evt.consume();
            JOptionPane.showMessageDialog(null, "SOLO DOS DIGIITOS");
        }
        if (solonumero < 18 && solonumero > 65) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(null, "EDAD NO VALIDA");
        }
    }//GEN-LAST:event_txt_ageKeyTyped

