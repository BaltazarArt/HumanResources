    void actualizar (){
        int id=Integer.parseInt(txt_id.getText());
        String firstName=txt_fn.getText();
        String lastName=txt_ln.getText();
        int age = Integer.parseInt(txt_age.getText());
        String email=txt_email.getText();
        int phoneNumber=Integer.parseInt(txt_phone.getText());
        Double salary=Double.parseDouble(txt_salary.getText());
        String hiredate=(txt_hd.getText().trim());
        String sql="UPDATE empleoyee set id='"+id+"',firstname='"+firstName+"',lastname='"+lastName+"',age='"+age+"',email='"
                +email+"',phoneNumber='"+phoneNumber+"',salary='"+salary+"',hiredate='"+hiredate+"'where id="+id;
                try {
                    cn=con.getConnection();
                    st=cn.createStatement();
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null,"Datos actualizazdos correctamente");
                    
                }catch (Exception e){
                    JOptionPane.showMessageDialog(null,"Datos no se guardaron correctamente");
                    limpiartable();
                }


    }
