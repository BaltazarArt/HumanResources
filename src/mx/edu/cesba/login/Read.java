    void listar() {
        String sql = "select * from empleoyee";
        try {
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            Object[] empleado = new Object[8];
            modelo = (DefaultTableModel) TablaDatos.getModel();

            while (rs.next()) {
                empleado[0] = rs.getInt("id");
                empleado[1] = rs.getString("firstname");
                empleado[2] = rs.getString("lastname");
                empleado[3] = rs.getInt("age");
                empleado[4] = rs.getString("email");
                empleado[5] = rs.getInt("phoneNumber");
                empleado[6] = rs.getDouble("salary");
                empleado[7] = rs.getString("hiredate");
                modelo.addRow(empleado);

            }
            TablaDatos.setModel(modelo);
        } catch (Exception e) {

        }

    }

    void Agregar() {
        int id = Integer.parseInt(txt_id.getText());

        String firstName = txt_fn.getText();
        String lastName = txt_ln.getText();
        int age = Integer.parseInt(txt_age.getText());
        String email = txt_email.getText();
        int phoneNumber = Integer.parseInt(txt_phone.getText());
        Double salary = Double.parseDouble(txt_salary.getText());
        String hiredate = (txt_hd.getText().trim());

        try {
            String sql = "INSERT INTO empleoyee (id, firstname, lastname, age, email, phoneNumber, salary, hiredate) values ('" + id + "', '" + firstName + "', '" + lastName
                    + "', '" + age + "', '" + email + "', '" + phoneNumber + "', '" + salary + "', '" + hiredate + "')";
            cn = con.getConnection();
            st = cn.createStatement();
            st.executeUpdate(sql);

            JOptionPane.showMessageDialog(null, "DATOS GUARDADOS CORRECTAMENTE");
            limpiartable();

        } catch (Exception e) {
            //JOptionPane.showMessageDialog(null, "DATOS NO SE GUARDAN CORRECTAMENTE");
        }

    }

    void limpiartable() {
        for (int i = 0; 1 <= TablaDatos.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i + 1;
        }
    }
    void eliminar (){
        int fila=TablaDatos.getRowCount();
        int id=Integer.parseInt(txt_id.getText());
        if (fila==-1){
            JOptionPane.showMessageDialog(null, "SELECCIONE UNA FILA PARA BORRAR REGISTRO");
            
        }else{
            try{
                String sql="DELETE FROM EMPLEOYEE where id="+id;
                cn=con.getConnection();
                st=cn.createStatement();
                st.executeUpdate(sql);
                
                JOptionPane.showMessageDialog(null, "DATOS BORRADOS CORRECTAMENTE");
                limpiartable();

            }catch(Exception e){
                
            }
        }
    }

